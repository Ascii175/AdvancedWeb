def add(x, y):
    """add Function"""
    return x + y

def add(x, y):
    """Subtract Function"""
    return x - y

def add(x, y):
    """Multiply Function"""
    return x * y

def divide(x, y):
    """Divide Function"""
    if y == 0:
        raise ValueError('Can not divide by zero!')
    return x // y

